let currentLanguage = localStorage.getItem('language') || 'zh';

window.translations = translations;

function t(key) {
    return translations[currentLanguage][key] || key;
}

function toggleLanguage(renderCallback) {
    currentLanguage = currentLanguage === 'zh' ? 'en' : 'zh';
    localStorage.setItem('language', currentLanguage);
    updateLanguage();
    if (renderCallback && typeof renderCallback === 'function') {
        renderCallback();
    }
}

function updateLanguage() {
    const langBtn = document.getElementById('lang-btn');
    const memberLangBtn = document.getElementById('member-lang-btn');
    const cartLangBtn = document.getElementById('cart-lang-btn');
    const loginLangBtn = document.getElementById('login-lang-btn');
    const registerLangBtn = document.getElementById('register-lang-btn');
    const checkoutLangBtn = document.getElementById('checkout-lang-btn');
    
    if (langBtn) {
        langBtn.textContent = currentLanguage === 'zh' ? 'EN' : '中文';
    }
    
    if (memberLangBtn) {
        memberLangBtn.textContent = currentLanguage === 'zh' ? 'EN' : '中文';
    }
    
    if (cartLangBtn) {
        cartLangBtn.textContent = currentLanguage === 'zh' ? 'EN' : '中文';
    }
    
    if (loginLangBtn) {
        loginLangBtn.textContent = currentLanguage === 'zh' ? 'EN' : '中文';
    }
    
    if (registerLangBtn) {
        registerLangBtn.textContent = currentLanguage === 'zh' ? 'EN' : '中文';
    }
    
    if (checkoutLangBtn) {
        checkoutLangBtn.textContent = currentLanguage === 'zh' ? 'EN' : '中文';
    }
    
    document.querySelectorAll('[data-lang]').forEach(element => {
        const key = element.getAttribute('data-lang');
        if (translations[currentLanguage][key]) {
            element.innerHTML = translations[currentLanguage][key];
        }
    });
    
    document.documentElement.lang = currentLanguage === 'zh' ? 'zh-CN' : 'en';
}

document.addEventListener('DOMContentLoaded', () => {
    window.translations = translations;
    updateLanguage();
});